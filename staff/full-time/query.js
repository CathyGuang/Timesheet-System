// Initialize a new plugin instance for all
// e.g. $('input[type="range"]') elements.

$('input[type="range"]').rangeslider({

    // Feature detection the default is `true`.
    // Set this to `false` if you want to use
    // the polyfill also in Browsers which support
    // the native <input type="range"> element.
    polyfill: false,

    onInit: function() {

        $(".output_hours").html(0)

    },

    // Callback function
    //values are in minutes, maximum value 720
    onSlide: function(position, value) {


        var num = 0;
        var targetName = this.$element[0].name;
        if(targetName == "range1")
            num = 0;
        else if(targetName == "range2")
            num = 1;
        else if(targetName == "range3")
            num = 2;
        else if(targetName == "range4")
            num = 3;
        else if(targetName == "range5")
            num = 4;
        else if(targetName == "range6")
            num = 5;


        minutes=value%60;
        

        hours_whole=(value-minutes)/60;

        if(hours_whole==0){
            $(".output_hours").eq(num).html(minutes+" minutes")

        }

        else{

            $(".output_hours").eq(num).html(hours_whole+" hours "+minutes+" minutes")

        }

        

    },
    
});
